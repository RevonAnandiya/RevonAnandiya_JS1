var hari = "Sabtu";

switch (hari) {
  case "Sabtu":
    console.log("Hari ini adalah hari Sabtu.");
    break;
  case "Minggu":
    console.log("Hari ini adalah hari Minggu.");
    break;
  case "Senin":
    console.log("Hari ini adalah hari Senin.");
    break;
  default:
    console.log("Ini bukan hari kerja.");
}